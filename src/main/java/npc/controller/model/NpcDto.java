package npc.controller.model;

public class NpcDto {

  private Long npcId;
  private Long name;
  private Long species;
  private Long background;
  private Long profession;
  private Long personality;
 
  public Long getNpcId() {
    return npcId;
  }
  public Long getName() {
    return name;
  }
  public Long getSpecies() {
    return species;
  }
  public Long getBackground() {
    return background;
  }
  public Long getProfession() {
    return profession;
  }
  public Long getPersonality() {
    return personality;
  }
  
  
  
}
